
const myString = "            This is a test.    ";

// The method trim() removes whitespace from both sides of a String.
console.log(myString.trim()); // Output | This is a test.



// The method trimStart() removes whitespace ONLY from the start of a String.
// NOTE: Make sure to highlight your cursor over the output to see the whitespace after ..."test.".
console.log(myString.trimStart()); // Output | This is a test.    



// The method trimEnd() removes whitespace ONLY from the end of a String.
console.log(myString.trimEnd()); // Output |             This is a test.